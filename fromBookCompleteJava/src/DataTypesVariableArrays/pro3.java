package DataTypesVariableArrays;
//char behave like int
public class pro3 {
    public static void main(String[] args) {
        char ch1 ='x';
        System.out.println("char ch1 "+ch1);
        ch1++;
        System.out.println();
        System.out.println("char ch1 "+ch1);
        ch1+=2;
        System.out.println("char ch1 "+ch1);
        ch1-=10;
        System.out.println("char ch1 "+ch1);
    }
}
